package principal.physics;

public class Position {

	private float x;
	private float y;
	
	public Position(float x, float y){
		this.x = x;
		this.y = y;
	}

	// setter y getters
	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}
	
	
	
}
